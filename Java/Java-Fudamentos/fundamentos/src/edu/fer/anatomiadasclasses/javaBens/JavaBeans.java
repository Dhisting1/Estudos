public class JavaBeans {
//    JavaBeans é um padrão de projeto que define regras para a criação de classes
//    Variáveis que começam em um idioma X as outras devem seguir o mesmo idioma
//    Não abreviar variáveis
//    Variáveis sempre no singular
//    Não recomendado:
    double salMedio = 1500.32;
    String emailS = "email@email.com";
    String myName = "João";
//    Recomendado:
    double salarioMedio = 1500.32;
    String email = "email@email.com";
    String [] emails= {"email1@email.com", "email2@email.com"};
    String meuNome = "João";


}
